package t99_wbs_plan.t04_t05_operatoren;

public class S03_ArithmetischeOperatoren {
  
  public static void main( String[] args ) {
    //     * /    %    + -
    int a = 3;
    int b = 5;
    System.out.println("============================================================");
    System.out.println("a: " + a); // 3
    System.out.println("b: " + b); // 5
    System.out.println("============================================================");
    int res = a + b;
    System.out.println("a + b: " + res); // 8
    System.out.println("============================================================");
    res = a * b;
    System.out.println("a * b: " + res); // 15
    System.out.println("============================================================");
    System.out.println("20 % 3: " + 20 % 3); // 2
    System.out.println("============================================================");
    
  }
  
}
